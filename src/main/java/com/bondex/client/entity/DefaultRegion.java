package com.bondex.client.entity;

import org.springframework.stereotype.Component;

@Component
public class DefaultRegion {
	private Integer id;
	private String opid;
	private Integer office_id;
	private String username;
	private Integer default_region_id;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getOpid() {
		return opid;
	}

	public void setOpid(String opid) {
		this.opid = opid;
	}

	public Integer getOffice_id() {
		return office_id;
	}

	public void setOffice_id(Integer office_id) {
		this.office_id = office_id;
	}

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	public Integer getDefault_region_id() {
		return default_region_id;
	}

	public void setDefault_region_id(Integer default_region_id) {
		this.default_region_id = default_region_id;
	}

}
