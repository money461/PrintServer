package com.bondex.security.entity;

/**
 * 接口返回字段(主要用于获取opid)
 * 
 * @version 2018年5月17日 16:16:19
 * @author bondex_public
 *
 */
public class OperatorIdResult {
	private String success;
	private String errormsg;
	private OperatorIdData[] message;

	public String getSuccess() {
		return success;
	}

	public void setSuccess(String success) {
		this.success = success;
	}

	public String getErrormsg() {
		return errormsg;
	}

	public void setErrormsg(String errormsg) {
		this.errormsg = errormsg;
	}

	public OperatorIdResult(String success, String errormsg, OperatorIdData[] message) {
		super();
		this.success = success;
		this.errormsg = errormsg;
		this.message = message;
	}

	public OperatorIdData[] getMessage() {
		return message;
	}

	public void setMessage(OperatorIdData[] message) {
		this.message = message;
	}

	public OperatorIdResult() {
		super();
	}

}
