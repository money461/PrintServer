package com.bondex.security.entity;

public class Opid {
	private String opid;
	private String username;

	public String getOpid() {
		return opid;
	}

	public void setOpid(String opid) {
		this.opid = opid;
	}

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	public Opid(String opid, String username) {
		super();
		this.opid = opid;
		this.username = username;
	}

	public Opid() {
		super();
	}

}
